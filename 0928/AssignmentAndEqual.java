class  AssignmentAndEqual
{
	public static void main(String[] args) 
	{
		 
//대입(assign)연산자와 같으지 비교(equal)연산자는 다름
//=는 대입이고. ==는 같은지 비교하는 것

int age ; 
boolean flag;
//age에 20이라는 값을 대입 원함 ㅜㅜ
//20= age 불가함
age =20;

//age의 값이 20과 같은지 비교하여 논리값을 flag에 대입원함
flag = age == 20;

System.out.println(age);
System.out.println(flag);

	}
}
